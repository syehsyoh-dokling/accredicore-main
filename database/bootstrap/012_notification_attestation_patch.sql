-- Notification and policy attestation patch
-- Adds a local notification workflow and explicit staff acknowledgment records
-- so manual UAT can verify approval routing, distribution, and staff attestation.

CREATE TABLE IF NOT EXISTS public.notification_types (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  type_key text NOT NULL UNIQUE,
  display_name text NOT NULL,
  description text
);

CREATE TABLE IF NOT EXISTS public.notifications (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  type_id integer REFERENCES public.notification_types(id) ON DELETE SET NULL,
  message text NOT NULL,
  metadata jsonb,
  is_read boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now(),
  expires_at timestamptz
);

CREATE TABLE IF NOT EXISTS public.notification_preferences (
  user_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  type_id integer NOT NULL REFERENCES public.notification_types(id) ON DELETE CASCADE,
  email_enabled boolean DEFAULT true,
  push_enabled boolean DEFAULT true,
  sms_enabled boolean DEFAULT false,
  PRIMARY KEY (user_id, type_id)
);

CREATE TABLE IF NOT EXISTS public.policy_attestations (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  policy_id uuid NOT NULL REFERENCES public.policies(id) ON DELETE CASCADE,
  user_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  requested_by uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  status text NOT NULL DEFAULT 'pending',
  instructions text,
  notes text,
  due_at timestamptz,
  requested_at timestamptz NOT NULL DEFAULT now(),
  acknowledged_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_policy_attestations_pending_unique
  ON public.policy_attestations(policy_id, user_id)
  WHERE status = 'pending';

CREATE INDEX IF NOT EXISTS idx_notifications_user_created
  ON public.notifications(user_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_policy_attestations_policy
  ON public.policy_attestations(policy_id, requested_at DESC);

INSERT INTO public.notification_types (type_key, display_name, description)
VALUES
  ('policy_approval', 'Policy Approval', 'Approval workflow and stage change notifications'),
  ('policy_attestation', 'Policy Attestation', 'Staff acknowledgment and attestation notifications'),
  ('system', 'System', 'General system notifications')
ON CONFLICT (type_key) DO UPDATE
SET
  display_name = EXCLUDED.display_name,
  description = EXCLUDED.description;

