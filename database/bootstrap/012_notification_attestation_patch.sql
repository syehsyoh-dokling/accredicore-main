CREATE OR REPLACE FUNCTION public.notify_users_by_role(
  p_role public.app_role,
  p_type_key text,
  p_message text,
  p_metadata jsonb DEFAULT NULL,
  p_expires_at timestamptz DEFAULT NULL
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_count integer := 0;
  v_user_id uuid;
BEGIN
  FOR v_user_id IN
    SELECT DISTINCT user_roles.user_id
    FROM public.user_roles
    WHERE user_roles.role = p_role
  LOOP
    PERFORM public.send_notification(
      v_user_id,
      p_type_key,
      p_message,
      p_metadata,
      p_expires_at
    );
    v_count := v_count + 1;
  END LOOP;

  RETURN v_count;
END;
$$;

CREATE OR REPLACE FUNCTION public.mark_notification_read(p_notification_id integer)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  UPDATE public.notifications
  SET is_read = true
  WHERE id = p_notification_id
    AND user_id = auth.uid();

  RETURN FOUND;
END;
$$;

CREATE OR REPLACE FUNCTION public.mark_all_notifications_read()
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_count integer;
BEGIN
  UPDATE public.notifications
  SET is_read = true
  WHERE user_id = auth.uid()
    AND is_read = false;

  GET DIAGNOSTICS v_count = ROW_COUNT;
  RETURN v_count;
END;
$$;

CREATE OR REPLACE FUNCTION public.create_policy_attestation(
  p_policy_id uuid,
  p_user_id uuid,
  p_due_at timestamptz DEFAULT NULL,
  p_instructions text DEFAULT NULL
)
RETURNS uuid
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_policy public.policies%ROWTYPE;
  v_attestation_id uuid;
BEGIN
  IF auth.uid() IS NULL THEN
    RAISE EXCEPTION 'Authentication required';
  END IF;

  IF NOT public.is_elevated_role() THEN
    RAISE EXCEPTION 'Only elevated roles can request attestations';
  END IF;

  SELECT *
  INTO v_policy
  FROM public.policies
  WHERE id = p_policy_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Policy % not found', p_policy_id;
  END IF;

  IF v_policy.status <> 'approved' THEN
    RAISE EXCEPTION 'Policy % must be approved before requesting attestation', p_policy_id;
  END IF;

  SELECT id
  INTO v_attestation_id
  FROM public.policy_attestations
  WHERE policy_id = p_policy_id
    AND user_id = p_user_id
    AND status = 'pending'
  ORDER BY requested_at DESC
  LIMIT 1;

  IF v_attestation_id IS NOT NULL THEN
    UPDATE public.policy_attestations
    SET
      instructions = p_instructions,
      due_at = p_due_at,
      requested_by = auth.uid(),
      requested_at = now(),
      notes = NULL
    WHERE id = v_attestation_id;
  ELSE
    UPDATE public.policy_attestations
    SET
      status = 'pending',
      instructions = p_instructions,
      due_at = p_due_at,
      requested_by = auth.uid(),
      requested_at = now(),
      acknowledged_at = NULL,
      notes = NULL
    WHERE policy_id = p_policy_id
      AND user_id = p_user_id
      AND status IN ('acknowledged', 'expired')
    RETURNING id INTO v_attestation_id;

    IF v_attestation_id IS NULL THEN
      INSERT INTO public.policy_attestations (
        policy_id,
        user_id,
        requested_by,
        instructions,
        due_at
      )
      VALUES (
        p_policy_id,
        p_user_id,
        auth.uid(),
        p_instructions,
        p_due_at
      )
      RETURNING id INTO v_attestation_id;
    END IF;
  END IF;

  RETURN v_attestation_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.acknowledge_policy_attestation(
  p_attestation_id uuid,
  p_notes text DEFAULT NULL
)
RETURNS uuid
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_attestation public.policy_attestations%ROWTYPE;
BEGIN
  IF auth.uid() IS NULL THEN
    RAISE EXCEPTION 'Authentication required';
  END IF;

  SELECT *
  INTO v_attestation
  FROM public.policy_attestations
  WHERE id = p_attestation_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Attestation % not found', p_attestation_id;
  END IF;

  IF v_attestation.status <> 'pending' THEN
    RAISE EXCEPTION 'Attestation % is already finalized', p_attestation_id;
  END IF;

  IF v_attestation.user_id <> auth.uid() AND NOT public.is_elevated_role() THEN
    RAISE EXCEPTION 'Only the assigned user can acknowledge this attestation';
  END IF;

  UPDATE public.policy_attestations
  SET
    status = 'acknowledged',
    acknowledged_at = now(),
    notes = COALESCE(p_notes, notes)
  WHERE id = p_attestation_id;

  RETURN p_attestation_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.get_policy_attestation_summary(p_policy_id uuid)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
AS $$
DECLARE
  v_result jsonb;
BEGIN
  SELECT jsonb_build_object(
    'policy_id', p_policy_id,
    'attestations', COALESCE(
      (
        SELECT jsonb_agg(
          jsonb_build_object(
            'id', pa.id,
            'policy_id', pa.policy_id,
            'user_id', pa.user_id,
            'requested_by', pa.requested_by,
            'status', pa.status,
            'instructions', pa.instructions,
            'notes', pa.notes,
            'due_at', pa.due_at,
            'requested_at', pa.requested_at,
            'acknowledged_at', pa.acknowledged_at,
            'user_name', assigned_profile.full_name,
            'user_email', assigned_profile.email,
            'requested_by_name', requester_profile.full_name
          )
          ORDER BY pa.requested_at DESC
        )
        FROM public.policy_attestations pa
        LEFT JOIN public.profiles assigned_profile
          ON assigned_profile.id = pa.user_id
        LEFT JOIN public.profiles requester_profile
          ON requester_profile.id = pa.requested_by
        WHERE pa.policy_id = p_policy_id
      ),
      '[]'::jsonb
    )
  )
  INTO v_result;

  RETURN v_result;
END;
$$;

CREATE OR REPLACE FUNCTION public.notify_policy_approval_change()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_policy_title text;
  v_message text;
BEGIN
  SELECT title INTO v_policy_title
  FROM public.policies
  WHERE id = COALESCE(NEW.policy_id, OLD.policy_id);

  IF TG_OP = 'INSERT' AND NEW.status = 'pending' THEN
    v_message := format(
      'Policy "%s" is waiting for %s.',
      COALESCE(v_policy_title, NEW.policy_id::text),
      replace(NEW.stage_name, '_', ' ')
    );

    PERFORM public.notify_users_by_role(
      NEW.required_role,
      'policy_approval',
      v_message,
      jsonb_build_object(
        'policy_id', NEW.policy_id,
        'approval_request_id', NEW.id,
        'stage_name', NEW.stage_name,
        'status', NEW.status
      )
    );
  ELSIF TG_OP = 'UPDATE' AND NEW.status IS DISTINCT FROM OLD.status THEN
    IF NEW.status = 'pending' THEN
      v_message := format(
        'Policy "%s" is now ready for your approval at %s.',
        COALESCE(v_policy_title, NEW.policy_id::text),
        replace(NEW.stage_name, '_', ' ')
      );

      PERFORM public.notify_users_by_role(
        NEW.required_role,
        'policy_approval',
        v_message,
        jsonb_build_object(
          'policy_id', NEW.policy_id,
          'approval_request_id', NEW.id,
          'stage_name', NEW.stage_name,
          'status', NEW.status
        )
      );
    END IF;

    IF NEW.status IN ('approved', 'rejected') THEN
      v_message := format(
        'Policy "%s" approval step "%s" was %s.',
        COALESCE(v_policy_title, NEW.policy_id::text),
        replace(NEW.stage_name, '_', ' '),
        NEW.status
      );

      PERFORM public.send_notification(
        NEW.requested_by,
        'policy_approval',
        v_message,
        jsonb_build_object(
          'policy_id', NEW.policy_id,
          'approval_request_id', NEW.id,
          'stage_name', NEW.stage_name,
          'status', NEW.status
        )
      );
    END IF;
  END IF;

  RETURN COALESCE(NEW, OLD);
END;
$$;

DROP TRIGGER IF EXISTS policy_approval_notifications ON public.policy_approval_requests;
CREATE TRIGGER policy_approval_notifications
AFTER INSERT OR UPDATE ON public.policy_approval_requests
FOR EACH ROW EXECUTE FUNCTION public.notify_policy_approval_change();

CREATE OR REPLACE FUNCTION public.notify_policy_status_change()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  IF NEW.status IS DISTINCT FROM OLD.status AND NEW.status = 'approved' THEN
    PERFORM public.send_notification(
      NEW.created_by,
      'policy_approval',
      format('Policy "%s" has been fully approved and is ready for staff acknowledgment.', NEW.title),
      jsonb_build_object(
        'policy_id', NEW.id,
        'status', NEW.status
      )
    );
  END IF;

  RETURN NEW;
END;
$$;

