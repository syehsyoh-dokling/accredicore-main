CREATE OR REPLACE FUNCTION public.send_notification(
  p_user_id uuid,
  p_type_key text,
  p_message text,
  p_metadata jsonb DEFAULT NULL,
  p_expires_at timestamptz DEFAULT NULL
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_type_id integer;
  v_notification_id integer;
BEGIN
  IF p_user_id IS NULL THEN
    RAISE EXCEPTION 'Notification recipient is required';
  END IF;

  SELECT id
  INTO v_type_id
  FROM public.notification_types
  WHERE type_key = COALESCE(p_type_key, 'system');

  IF v_type_id IS NULL THEN
    INSERT INTO public.notification_types(type_key, display_name, description)
    VALUES (
      COALESCE(p_type_key, 'system'),
      initcap(replace(COALESCE(p_type_key, 'system'), '_', ' ')),
      'Auto-created local notification type'
    )
    RETURNING id INTO v_type_id;
  END IF;

  INSERT INTO public.notifications (
    user_id,
    type_id,
    message,
    metadata,
    expires_at
  )
  VALUES (
    p_user_id,
    v_type_id,
    p_message,
    COALESCE(p_metadata, '{}'::jsonb),
    p_expires_at
  )
  RETURNING id INTO v_notification_id;

  RETURN v_notification_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.send_notification(
  p_message text,
  p_type_name text,
  p_user_id uuid
)
RETURNS void
LANGUAGE plpgsql
