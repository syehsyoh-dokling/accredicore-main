CREATE TRIGGER policy_status_notifications
AFTER UPDATE ON public.policies
FOR EACH ROW EXECUTE FUNCTION public.notify_policy_status_change();

CREATE OR REPLACE FUNCTION public.notify_policy_attestation_change()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_policy_title text;
BEGIN
  SELECT title INTO v_policy_title
  FROM public.policies
  WHERE id = COALESCE(NEW.policy_id, OLD.policy_id);

  IF TG_OP = 'INSERT' THEN
    PERFORM public.send_notification(
      NEW.user_id,
      'policy_attestation',
      format('Please acknowledge policy "%s".', COALESCE(v_policy_title, NEW.policy_id::text)),
      jsonb_build_object(
        'policy_id', NEW.policy_id,
        'attestation_id', NEW.id,
        'status', NEW.status
      ),
      NEW.due_at
    );
  ELSIF TG_OP = 'UPDATE' AND NEW.status IS DISTINCT FROM OLD.status AND NEW.status = 'acknowledged' THEN
    PERFORM public.send_notification(
      NEW.requested_by,
      'policy_attestation',
      format('User acknowledgment completed for policy "%s".', COALESCE(v_policy_title, NEW.policy_id::text)),
      jsonb_build_object(
        'policy_id', NEW.policy_id,
        'attestation_id', NEW.id,
        'status', NEW.status,
        'user_id', NEW.user_id
      )
    );
  END IF;

  RETURN COALESCE(NEW, OLD);
END;
$$;

DROP TRIGGER IF EXISTS policy_attestation_notifications ON public.policy_attestations;
CREATE TRIGGER policy_attestation_notifications
AFTER INSERT OR UPDATE ON public.policy_attestations
FOR EACH ROW EXECUTE FUNCTION public.notify_policy_attestation_change();
