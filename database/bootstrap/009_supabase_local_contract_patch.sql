CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = ''
AS $$
BEGIN
  INSERT INTO public.profiles (id, email, full_name)
  VALUES (
    NEW.id,
    NEW.email,
    COALESCE(NEW.raw_user_meta_data ->> 'full_name', NEW.email)
  )
  ON CONFLICT (id) DO UPDATE
  SET email = EXCLUDED.email,
      full_name = COALESCE(EXCLUDED.full_name, public.profiles.full_name);

  INSERT INTO public.user_roles (user_id, role)
  VALUES (NEW.id, 'user'::public.app_role)
  ON CONFLICT (user_id, role) DO NOTHING;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
AFTER INSERT ON auth.users
FOR EACH ROW
EXECUTE FUNCTION public.handle_new_user();

CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.has_role(_user_id uuid, _role public.app_role)
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.user_roles
    WHERE user_id = _user_id
      AND role = _role
  );
$$;

CREATE OR REPLACE FUNCTION public.get_current_user_role()
RETURNS public.app_role
LANGUAGE sql
STABLE
SECURITY DEFINER
AS $$
  SELECT COALESCE(
    (
      SELECT role
      FROM public.user_roles
      WHERE user_id = auth.uid()
      ORDER BY
        CASE role
          WHEN 'system_admin' THEN 1
          WHEN 'super_user' THEN 2
          WHEN 'admin' THEN 3
          WHEN 'developer' THEN 4
          WHEN 'team' THEN 5
          WHEN 'client' THEN 6
          ELSE 7
        END
      LIMIT 1
    ),
    'user'::public.app_role
  );
$$;

CREATE OR REPLACE FUNCTION public.get_department_compliance_summary(dept_id integer DEFAULT NULL)
RETURNS TABLE (
  department_name text,
  total_controls bigint,
  completed_controls bigint,
  in_progress_controls bigint,
  not_started_controls bigint,
  compliance_percentage numeric
)
LANGUAGE sql
STABLE
SECURITY DEFINER
AS $$
  SELECT
    d.name AS department_name,
    COUNT(c.id)::bigint AS total_controls,
    COUNT(*) FILTER (WHERE ap.status = 'completed')::bigint AS completed_controls,
    COUNT(*) FILTER (WHERE ap.status = 'in_progress')::bigint AS in_progress_controls,
    COUNT(*) FILTER (
      WHERE ap.status IS NULL OR ap.status IN ('not_started', 'pending')
    )::bigint AS not_started_controls,
    CASE
      WHEN COUNT(c.id) = 0 THEN 0
      ELSE ROUND(
        (COUNT(*) FILTER (WHERE ap.status = 'completed')::numeric / COUNT(c.id)::numeric) * 100,
        2
      )
    END AS compliance_percentage
  FROM public.departments d
  CROSS JOIN public.controls c
  LEFT JOIN public.facilities f
    ON f.department_id = d.id
   AND f.is_active = true
  LEFT JOIN public.audit_progress ap
    ON ap.facility_id = f.id
   AND ap.control_id = c.id
  WHERE d.is_active = true
    AND (dept_id IS NULL OR d.id = dept_id)
  GROUP BY d.id, d.name
  ORDER BY d.name;
$$;

CREATE OR REPLACE FUNCTION public.update_audit_progress(
  p_facility_id integer,
  p_user_id uuid,
  p_control_id bigint,
  p_status text,
  p_progress_percentage integer,
  p_notes text DEFAULT NULL
)
RETURNS TABLE (
  id integer,
  progress_percentage integer,
  status text
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_row public.audit_progress%ROWTYPE;
BEGIN
  INSERT INTO public.audit_progress (
    facility_id,
    user_id,
    control_id,
    status,
    progress_percentage,
    notes,
    completed_at,
    updated_at
  )
  VALUES (
    p_facility_id,
    p_user_id,
    p_control_id,
    p_status,
    p_progress_percentage,
    p_notes,
    CASE WHEN p_status = 'completed' THEN now() ELSE NULL END,
    now()
  )
  ON CONFLICT (facility_id, control_id)
  DO UPDATE SET
    user_id = EXCLUDED.user_id,
    status = EXCLUDED.status,
    progress_percentage = EXCLUDED.progress_percentage,
    notes = EXCLUDED.notes,
    completed_at = CASE
      WHEN EXCLUDED.status = 'completed' THEN now()
      ELSE public.audit_progress.completed_at
    END,
    updated_at = now()
  RETURNING * INTO v_row;

  RETURN QUERY
  SELECT v_row.id, v_row.progress_percentage, v_row.status;
END;
$$;

INSERT INTO storage.buckets (id, name, public)
VALUES
  ('documents', 'documents', true),
  ('images', 'images', true)
ON CONFLICT (id) DO NOTHING;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_policies
    WHERE schemaname = 'storage'
      AND tablename = 'objects'
      AND policyname = 'Local documents read'
  ) THEN
    CREATE POLICY "Local documents read"
    ON storage.objects
    FOR SELECT
    TO public
    USING (bucket_id IN ('documents', 'images'));
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM pg_policies
    WHERE schemaname = 'storage'
      AND tablename = 'objects'
      AND policyname = 'Local documents insert'
  ) THEN
    CREATE POLICY "Local documents insert"
    ON storage.objects
    FOR INSERT
    TO authenticated
    WITH CHECK (bucket_id IN ('documents', 'images'));
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM pg_policies
    WHERE schemaname = 'storage'
      AND tablename = 'objects'
      AND policyname = 'Local documents update'
  ) THEN
    CREATE POLICY "Local documents update"
    ON storage.objects
    FOR UPDATE
    TO authenticated
    USING (bucket_id IN ('documents', 'images'))
    WITH CHECK (bucket_id IN ('documents', 'images'));
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM pg_policies
    WHERE schemaname = 'storage'
      AND tablename = 'objects'
      AND policyname = 'Local documents delete'
  ) THEN
    CREATE POLICY "Local documents delete"
    ON storage.objects
    FOR DELETE
    TO authenticated
    USING (bucket_id IN ('documents', 'images'));
  END IF;
END
$$;

